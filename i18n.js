/**
 * i18n 키로 현재 언어의 텍스트를 반환.
 * @param {string} key
 * @param {string} [lang] - 생략 시 현재 선택 언어
 * @returns {string}
 */
function t(key, lang) {
  lang = lang || getSelectedOutputLang();
  const dict = I18N[lang] || I18N.ko;
  return dict[key] || I18N.ko[key] || key;
}


/**
 * 페이지 전체 UI 텍스트를 선택된 언어로 교체.
 * data-i18n, data-i18n-placeholder, data-i18n-title 속성을 처리한다.
 * @param {string} lang - 'ko' | 'en' | 'ja' | 'zh-CN' | 'zh-TW'
 */
function applyI18n(lang) {
  lang = lang || getSelectedOutputLang();
  const dict = I18N[lang] || I18N.ko;
  const fallback = I18N.ko;

  // data-i18n: textContent 교체
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = dict[key] || fallback[key] || el.textContent;
  });

  // data-i18n-placeholder: placeholder 교체
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = dict[key] || fallback[key] || el.placeholder;
  });

  // data-i18n-title: title 속성 교체
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = dict[key] || fallback[key] || el.title;
  });

  // data-i18n-html: innerHTML 교체 (manual-info 등)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = dict[key] || fallback[key] || el.innerHTML;
  });

  // 페이지 제목
  document.title = dict.page_title || fallback.page_title;

  // html lang 속성
  const htmlLang = { ko: 'ko', en: 'en', ja: 'ja', 'zh-CN': 'zh-CN', 'zh-TW': 'zh-TW' };
  document.documentElement.lang = htmlLang[lang] || 'ko';

  // 언어별 폰트 전환 — 해당 언어에 최적화된 Noto 폰트를 우선 적용
  const FONT_STACKS = {
    ko:      "'Pretendard', 'Noto Sans KR', -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif",
    en:      "-apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, 'Noto Sans KR', sans-serif",
    ja:      "'Noto Sans JP', 'Noto Sans KR', -apple-system, BlinkMacSystemFont, sans-serif",
    'zh-CN': "'Noto Sans SC', 'Noto Sans KR', -apple-system, BlinkMacSystemFont, sans-serif",
    'zh-TW': "'Noto Sans TC', 'Noto Sans KR', -apple-system, BlinkMacSystemFont, sans-serif",
  };
  const HANJA_STACKS = {
    ko:      "'Noto Serif KR', 'Noto Serif JP', 'Noto Serif SC', 'Noto Serif TC', 'Batang', serif",
    en:      "'Noto Serif KR', 'Noto Serif JP', 'Noto Serif SC', 'Noto Serif TC', serif",
    ja:      "'Noto Serif JP', 'Noto Serif KR', 'Noto Serif SC', serif",
    'zh-CN': "'Noto Serif SC', 'Noto Serif KR', 'Noto Serif JP', serif",
    'zh-TW': "'Noto Serif TC', 'Noto Serif KR', 'Noto Serif JP', serif",
  };
  const root = document.documentElement;
  root.style.setProperty('--font-stack', FONT_STACKS[lang] || FONT_STACKS.ko);
  root.style.setProperty('--font-hanja', HANJA_STACKS[lang] || HANJA_STACKS.ko);
}
